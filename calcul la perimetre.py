# Algorithme de calcul du périmètre d'un rectangle 
longueur = float(input("Entrez la longueur du rectangle : "))

largeur = float(input("Entrez la largeur du rectangle : "))

perimetre = 2 * (longueur + largeur)

surface = longueur*largeur

print("Le périmètre du rectangle est :", perimetre)

print("Le surface de rectangle est:", surface)
